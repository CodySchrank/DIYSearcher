				<p>
				This toaster was not designed to come apart, but the plastic case must come off. In this step I stripped off what I didn't want and wasn't going to use <br>

On the underside are Torx screws for which I did not have bits. The two prominent screws were removed with pliers, the others had the brown plastic hacked away from them until I could finish the job with pliers. <br>

The two control knobs are friction-fits on this machine and only required a good pull to get them off <br>

Whilst doing this I observed that the outer shell is rather hard and must have good heat resistance. To some extent the toaster self-cools by natural convection, the last photograph shows the the comparison between inner and outer. <br>

I wasn't going to be able to rely on this type of cooling or plastic in the video. <br>
